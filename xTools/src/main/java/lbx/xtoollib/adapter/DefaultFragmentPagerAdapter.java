public class DefaultFragmentPagerAdapter<T extends Fragment> extends FragmentPagerAdapter {

    private List<T> mList;

    public DefaultFragmentPagerAdapter(FragmentActivity activity, List<T> list) {
        super(activity.getSupportFragmentManager());
        mList = list;
    }

    @Override
    public T getItem(int position) {
        return mList.get(position);
    }

    @Override
    public int getCount() {
        return mList.size();
    }
}
